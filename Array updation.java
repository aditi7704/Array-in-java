package Aditi;
//Array updation 
public class P37 {
	public static void main(String[] args) {
        int[] arr = {1, 2, 3, 4, 5};

        // Before update
        System.out.println("Before update: " + arr[2]);

        // Updating value at index 2
        arr[2] = 99;

        // After update
        System.out.println("After update: " + arr[2]);
    }
}
